    <h2 class="text-center mb-4">CONNAITRE LE MOUVEMENT CONGO EN- AVANT (MCA)</h2>
    <h5 class="text-center mb-4"> <em>Qui sommes-nous ❓</em></h5>
    <p style="text-align:justify;">
    Un Méga Mouvement associatif à caractère politique et citoyen des leaders acteurs du
    développement de la RDC à partir de la Base.
    <p style="text-align:justify;">Le MCA, se veut un Mouvement national, qui rassemble les congolaises et congolais autour des 12
    valeurs citoyennes et républicaines qui permettent une meilleure gouvernance afin de sortir le
    peuple congolais de la misère. Notre approche est d’impulser le développement du pays à partir de
    la base. </p> 
    <p style="text-align:justify;">Le MCA apporte une révolution dans la gouvernance. Depuis l’accession de notre pays la RDC à
    l’indépendance, les gouvernements qui se sont succédés ont négligé le développement à la base au
    profit du sommet. Toutes les actions Gouvernementales étaient concentrées au sommet, pour
    l’élite, mettant ainsi le besoin de la base au dernier plan.
    <strong>La base représente nos communautés, le peuple congolais reparti dans les fins fonds de la
    RDC, les institutions locales qui sont les plus proches de la population (quartiers, secteurs,
    territoires, communes), les services qui interagissent directement avec les citoyens.</strong>
    Le MCA apporte un nouveau paradigme dans la gestion de la chose publique, une gouvernance
    tournée essentiellement vers le bien être de l’homme congolais.
    Le MCA redonne la fierté d’être appelé Congolais. C’est un mouvement d’avenir, futuriste qui se
    projette dans une vision d’un avenir meilleur pour la RDC et son peuple. </p>

   
    <h5 class="text-center mb-4"><em>D’où nous venons ❓</em></h5>
    <p style="text-align:justify;" >
        Le Mouvement Congo en-Avant est mis en place par un groupe des jeunes issus des rangs de
        l’UDPS/TSHIBALA. Après la démission du président KAS KASONGO de son poste de
        Président de la Ligue des jeunes et de l’UDPS/TSHIBALA, pour divergence politique, il s’en est
        suivi des démissions de plusieurs cadres et membres de ce parti qui ont préférés soutenir leur
        camarade. Ces derniers se sont décidés de mettre en place un mouvement en accord avec leur
        vision. D’où la naissance du MCA.
        </p>

    <h5 class="text-center mb-4"><em>Nos convictions</em></h5>
    <p style="text-align:justify;" >
    Le mouvement défend des convictions sociales démocraties et progressistes. Il encourage les
    réussites individuelles, tout en préservant les valeurs essentielles à la bonne gouvernance; une
    société qui garantisse la sécurité et la liberté de chacun, essentielles à toute projection dans l’avenir.
    Le mouvement défend la valeur travail comme facteur d’émancipation et de développement de la
    RDC.
        </p>
        <h5 class="text-center mb-4"><em>Notre vision</em></h5>
    <p style="text-align:justify;" >
    Notre vision est de faire du MCA un méga Mouvement national de la RDC afin d’être au cœur de
    la politique décisionnelle de notre pays afin d’exercer le pouvoir au plus haut sommet et ainsi
    implanter notre modèle de bonne gouvernance.
        </p>
        <h5 class="text-center mb-4"><em>Nos objectifs</em></h5>
    <p style="text-align:justify;" >
        L’objectif principal du MCA est de Booster l’émergence de la RDC par le développement à partir
    de la base pour assurer le bien-être des Congolais. <br>
    <strong>Le MCA se fixe comme objectifs spécifiques de :</strong><br>
    👉 Promouvoir les jeunes comme moteur de développement indispensables ;<br>
    👉 Encadrer, former et orienter ses membres ;<br>
    👉Lutter contre toutes les antivaleurs qui plombent le développement de la RDC, notamment,<br>
    la corruption, les détournements, l’impunité, le tribalisme, la paresse, l’oisiveté, la haine ;<br>
    👉 Utilisation stratégique de la jeunesse pour la création des richesses nationales ;<br>
    👉 Développer les compétences techniques et intellectuelles de ses membres ;<br>
    👉Lutter contre la pauvreté sous toutes ses formes par le travail ;<br>
    👉 Promouvoir et implémenter les 12 valeurs du mouvement ;<br>
    👉 Emanciper la femme, épanouir l’enfant et promouvoir la famille, notamment par
    l’organisation des séances de sensibilisation aux droits de la femme et de la famille, d’une
    part, et l’apprentissage des métiers et la création des coopératives de développement,
    d’autre part. <br>
    <strong>Nos 12 valeurs citoyennes et républicaines</strong><br>
    Pour atteindre ses objectifs, le MCA est assis sur l’échelle de douze valeurs citoyennes et
    républicaines ci-après : <br>
    1. Exemplarité : Liée à l’éthique, interdits tel que corruption, peau de vin, conflit d’intérêt,
    être un modèle dans la société ;<br>
    2. Esprit d’équipe : Un travail collectif pour aboutir aux objectifs assignés ; <br>
    3. Engagement national : S’interdire le tribalisme, le régionalisme et toute sorte de
    discrimination territoriale et familiale, nous sommes une nation, un peuple ; <br>
    4. Engagement citoyen : participation du citoyen dans la vie de sa communauté et être agent
    du développement local ; <br>
    5. Engagement Patriotique : Engagement à la défense de sa patrie, intégrité, les intérêts de
    toutes les communautés de sa patrie, défendre l’image de la patrie vis-à-vis de l’extérieur,
    défendre la RDC à tout prix ; <br>
    6. Egalité : de tous devant les obligations et les droits, égalité des opportunités entre homme
    et femme ; <br>
    7. Solidarité : Assistance des personnes vulnérables, en cas de catastrophes ; <br>
    8. Devoir de conscience : écouter sa conscience pour poser des actes acceptables par la société ; <br>
    9. Le travail : C’est par le travail que nous allons développer le pays et l’homme congolais. Il
    assure l’indépendance sociale et citoyenne La justice : La justice élève une nation, une justice
    juste et pour tous ; <br>
    10. La discipline : Respecter l’ordre et les règles établis, obéir aux règles de la société et du
    MCA. Avoir une maitrise de soi, s’imposer un comportement discipliné; <br>
    11. La transparence : Accès facile à l’information ; <br>
    12. La justice : la justice élève une nation, elle doit être impartiale et juste. La campagne le
    peuple réclame justice.
        </p>
