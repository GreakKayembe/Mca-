        <h5 class="text-center mb-4"><em>Notre organisation</em></h5>
    <p style="text-align:justify;" >
    Le MCA est dirigé par un Président qui s’occupe de la gestion politique du Mouvement. Il est
    encadré par la Haute Délégation Nationale qui est l’organe d’orientation et de stratégie du
    Mouvement. Cet organe est chapeauté par un Doyen. <br>
    La gestion administrative courante du MCA est assurée par un Délégué Général, qui est assisté par
    3 Délégués Généraux Adjoints et secondé par une équipe des Délégués Nationaux chefs de
    départements. <br> Il y a également un Trésorier et son Adjoint qui s’occupe des finances du MCA.
    Une importance particulière est accordée aux représentants du MCA à la base, car c’est le point de
    recrutement, des collectes des informations et préoccupations des citoyens congolais. <br>
