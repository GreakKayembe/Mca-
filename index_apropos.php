    👉Les premiers Délégués communaux ou territoriaux ; <br>
    👉Les Délégués communaux ou territoriaux ; <br>
    👉Les Délégués locaux au niveau des quartiers. <br>
    Au niveau des pays étrangers, le MCA est organisé de la manière suivante : <br>
    👉Le Premier Délégué du Pays ; <br>
    👉Les Délégués du pays chargés des thématiques ;<br>
    👉Les Délégués Villes ; <br>
